def generate_knowledge_map(topic, resources):
    content = f"# Knowledge Map: {topic}\n\n"
    content += "This document is automatically generated by the Didactic Barnacle engine.\n\n"
        
    categories = {"Beginner": [], "Intermediate": [], "Advanced": []}
    for res in resources:
        category = categorize_resource(res['title'], res['url'])
        categories[category].append(res)

    for category_name in ["Beginner", "Intermediate", "Advanced"]:
        if categories[category_name]:
            content += f"## {category_name}\n\n"
            for res in categories[category_name]:
                content += f"*   [{res['title']}]({res['url']})\n"
            content += "\n"
        
    return content

def main():
    resources = scrape_resources(TOPIC)
    if resources:
        knowledge_map_content = generate_knowledge_map(TOPIC, resources)
        with open(OUTPUT_FILE, "w", encoding="utf-8") as f:
            f.write(knowledge_map_content)
        print(f"Knowledge map generated: {OUTPUT_FILE}")
    else:
        print("No resources found or an error occurred.")

if __name__ == "__main__":
    main()
  
